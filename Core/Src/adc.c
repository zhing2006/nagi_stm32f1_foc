void HAL_ADCEx_InjectedConvCpltCallback(ADC_HandleTypeDef *hadc) {
  if (hadc->Instance == ADC1) {
    // Ignore if the motor is not calibrated.
    if (!g_motor.is_calibrated) {
      return;
    }

    static float v_a_offset = 0.0f;
    static float v_b_offset = 0.0f;
    static uint16_t count = 0;
    float v_a = ADC_REF_VOLTAGE * ((float)HAL_ADCEx_InjectedGetValue(&hadc1, ADC_INJECTED_RANK_1) / ((1 << ADC_BITS) - 1) - 0.5);
    float v_b = ADC_REF_VOLTAGE * ((float)HAL_ADCEx_InjectedGetValue(&hadc2, ADC_INJECTED_RANK_1) / ((1 << ADC_BITS) - 1) - 0.5);

    // Calibrate the offset.
    if (count < 1000) {
      v_a_offset += v_a;
      v_b_offset += v_b;
      count++;
      return;
    } else if (count == 1000) {
      v_a_offset /= 1000.0f;
      v_b_offset /= 1000.0f;
      count++;
    }

    v_a -= v_a_offset;
    v_b -= v_b_offset;
    float i_a = v_a / CURRENT_R_SHUNT / CURRENT_OP_GAIN;
    float i_b = v_b / CURRENT_R_SHUNT / CURRENT_OP_GAIN;
    float i_d, i_q;
    nagi_foc_motor_calculate_current(&g_motor, i_a, i_b, &i_d, &i_q);
    g_motor.i_d = low_pass_filter_update(&g_current_d_filter, i_d);
    g_motor.i_q = low_pass_filter_update(&g_current_q_filter, i_q);

    if (g_motor.is_logical_angle_ready && g_motor.is_speed_ready) {
      switch (g_control_context.type) {
      case CONTROL_TYPE_POSITION:
        nagi_foc_motor_position_control(&g_motor, g_control_context.position);
        break;
      case CONTROL_TYPE_SPEED:
        nagi_foc_motor_speed_control(&g_motor, g_control_context.speed);
        break;
      case CONTROL_TYPE_CURRENT:
        nagi_foc_motor_current_control(&g_motor, g_control_context.current_norm_d, g_control_context.current_norm_q);
        break;
      case CONTROL_TYPE_SPEED_CURRENT:
        nagi_foc_motor_speed_current_control(&g_motor, g_control_context.speed, g_control_context.max_current);
        break;
      case CONTROL_TYPE_POSITION_SPEED_CURRENT:
        nagi_foc_motor_position_speed_current_control(&g_motor, g_control_context.position, g_control_context.max_speed, g_control_context.max_current);
        break;
      default:
        break;
      }
    }
  }
}
